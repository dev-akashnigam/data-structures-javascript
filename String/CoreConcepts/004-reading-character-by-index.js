let str = "Hello World";

const firstChar = str.charAt(0);
console.log(`First character in string: ${str} = ${firstChar}`);

const fifthChar = str.charAt(4);
console.log(`Fifth character in string: ${str} = ${fifthChar}`);

const lastChar = str.charAt(str.length-1);
console.log(`Last character in string: ${str} = ${lastChar}`);