let arr = [10, 20, 30, 40, 50];

const firstElement = arr[0];
const forthElement = arr[3];
const lastElement = arr[arr.length-1];

const elementAtFirstPlace = arr.at(0);
const elementAtForthPlace = arr.at(3);
const elementAtLastPlace = arr.at(-1);

console.log(`First Element of Array = ${firstElement}`);
console.log(`Forth Element of Array = ${forthElement}`);
console.log(`Last Element of Array = ${lastElement}`);

console.log(`Element of Array At First Place = ${elementAtFirstPlace}`);
console.log(`Element of Array At Forth Place = ${elementAtForthPlace}`);
console.log(`Element of Array At Last Place = ${elementAtLastPlace}`);


